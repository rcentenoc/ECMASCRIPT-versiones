// replace all sirve para reemplazar todas las ocurrencias de un string por otro
// en vez de usar una expresion regular

const string = "Fabricio es un gran programador y pronto se va bachillerar para scar su título";
const newString = string.replaceAll("Fabricio", "Ronald");
console.log(newString); // Ronald es un gran programador y pronto se va bachillerar para scar su título

