let hello = 'hello';
let world = 'world';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

// Template literals
let epicPhrase2 = `${hello} ${world}!`; // utiliza comillas francesas ``
console.log(epicPhrase2);

// Multi-line strings
let lorem = 'Esto es un string \n'+'esto es una segunda linea';

let lorem2 = `Esta es una oración. 
y esta es otra oración.`;

console.log(lorem);
console.log(lorem2);